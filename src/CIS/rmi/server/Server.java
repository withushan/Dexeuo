    /****
     * This method contains the code for the stop button visible in the Server GUI.
     * This button will help to stop the Server
     * @param evt 
     */
    private void btnStopServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStopServerActionPerformed

        //This message will popup after clicking the stop button
        int responseStop = JOptionPane.showConfirmDialog(this, "Do you want to continue action ?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        // If you click "yes" the server will stop
        if(responseStop == JOptionPane.YES_OPTION){
            System.exit(0);
        }
        else
        {
            //If you click "NO" the server will prints this message
            JOptionPane.showMessageDialog(this, "Action is cancelled!");
        }
    }//GEN-LAST:event_btnStopServerActionPerformed

