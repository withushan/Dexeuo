    private void btnStartServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartServerActionPerformed
        // TODO add your handling code here:
        
        //This message will visible after clicking the "Start" button
        S1.setText("Starting the Student Server 2020");
        
        try {
            
            /* The suspect that this block of statement can throw 
             * exception so we handled it by placing these statements
             * inside try and handled the exception in catch block
             */
            Interface impl = new ClientImplementation();
            Registry reg = LocateRegistry.createRegistry(1099);
            reg.rebind("Server" , impl);
            
            S2.setText("Server Started");
            S3.setText("Welcome to Student Feeedback System");
            btnStopServer.setEnabled(true);
        }catch(RemoteException e){
            
            /* This block will only execute if any Remote exception 
             * occurs in try block
            */
            
            //The label will print this error message
            S2.setText("A Problem occured");
            S3.setText("Your Server is already running");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnStartServerActionPerformed

